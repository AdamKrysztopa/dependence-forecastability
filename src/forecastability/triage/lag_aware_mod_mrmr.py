class SelectedLagAwareFeature(BaseModel):
    """A lagged covariate candidate that was selected by the greedy selector.

    Attributes:
        covariate_name: Name of the covariate series.
        lag: Lag value k.
        is_known_future: Whether this covariate was declared as known-future.
        known_future_provenance: Provenance label when is_known_future=True;
            ``None`` for ordinary measured covariates.
        legality_reason: Legality classification label (``legal`` or
            ``legal_known_future``).
        feature_name: Derived feature identifier.
        relevance: Normalized relevance score against the target; >= 0.
        max_redundancy: Maximum normalized similarity against already-selected
            features; in [0, 1].
        target_history_redundancy: Maximum normalized similarity against
            target-history lags; in [0, 1]. Zero when target-history novelty
            penalty is disabled.
        final_score: Greedy selection score computed as
            ``relevance * (1 - max_redundancy) * (1 - target_history_redundancy)``;
            clipped at 0.
        selection_rank: 1-based position in the greedy selection order.
        relevance_scorer_name: Name of the scorer used for relevance.
        redundancy_scorer_name: Name of the scorer used for redundancy.
        target_history_scorer_name: Name of the scorer used for target-history
            novelty; ``None`` when the penalty is disabled.
        normalization_strategy: Normalization strategy applied across all
            penalty terms in this run.
        relevance_diagnostics: Full diagnostic payload from the relevance scorer;
            ``None`` when diagnostics were not collected.
        redundancy_diagnostics: Full diagnostic payload from the redundancy
            scorer; ``None`` when the candidate was the first selected (no
            redundancy pair exists) or diagnostics were not collected.
        target_history_diagnostics: Full diagnostic payload from the
            target-history scorer; ``None`` when the penalty is disabled or
            diagnostics were not collected.
    """

    model_config = ConfigDict(frozen=True)

    covariate_name: str = Field(description="Name of the covariate series.")
    lag: int = Field(ge=0, description="Lag value k.")
    is_known_future: bool = Field(
        default=False,
        description="Whether this covariate was declared as known-future.",
    )
    known_future_provenance: KnownFutureProvenance | None = Field(
        default=None,
        description="Provenance label when is_known_future=True; None otherwise.",
    )
    legality_reason: LagLegalityLabel = Field(
        description="Legality classification label (legal or legal_known_future).",
    )
    feature_name: str = Field(description="Derived feature identifier.")
    relevance: float = Field(ge=0.0, description="Normalized relevance score against the target.")
    max_redundancy: float = Field(
        ge=0.0,
        le=1.0,
        description=("Maximum normalized similarity against already-selected features; in [0, 1]."),
    )
    target_history_redundancy: float = Field(
        ge=0.0,
        le=1.0,
        description=(
            "Maximum normalized similarity against target-history lags; in [0, 1]. "
            "Zero when target-history novelty penalty is disabled."
        ),
    )
    final_score: float = Field(
        ge=0.0,
        description=(
            "Greedy selection score: "
            "relevance * (1 - max_redundancy) * (1 - target_history_redundancy)."
        ),
    )
    selection_rank: int = Field(
        ge=1,
        description="1-based position in the greedy selection order.",
    )
    relevance_scorer_name: str = Field(description="Name of the scorer used for relevance.")
    redundancy_scorer_name: str = Field(description="Name of the scorer used for redundancy.")
    target_history_scorer_name: str | None = Field(
        default=None,
        description=(
            "Name of the scorer used for target-history novelty; None when the penalty is disabled."
        ),
    )
    normalization_strategy: NormalizationStrategy = Field(
        description="Normalization strategy applied across all penalty terms.",
    )
    relevance_diagnostics: ScorerDiagnostics | None = Field(
        default=None,
        description="Relevance scorer diagnostic payload; None when not collected.",
    )
    redundancy_diagnostics: ScorerDiagnostics | None = Field(
        default=None,
        description=(
            "Redundancy scorer diagnostic payload; None for the first selected "
            "feature or when not collected."
        ),
    )
    target_history_diagnostics: ScorerDiagnostics | None = Field(
        default=None,
        description=(
            "Target-history scorer diagnostic payload; None when penalty is disabled "
            "or not collected."
        ),
    )

    @model_validator(mode="after")
    def _validate_legality_is_legal(self) -> SelectedLagAwareFeature:
        """Selected features must have a legal legality classification."""
        if self.legality_reason not in ("legal", "legal_known_future"):
            raise ValueError(
                f"SelectedLagAwareFeature.legality_reason must be 'legal' or "
                f"'legal_known_future', got {self.legality_reason!r}"
            )
        return self

    @model_validator(mode="after")
    def _validate_known_future_consistency(self) -> SelectedLagAwareFeature:
        """Require provenance when is_known_future=True and vice-versa."""
        if self.is_known_future and self.known_future_provenance is None:
            raise ValueError("known_future_provenance must be set when is_known_future=True")
        if not self.is_known_future and self.known_future_provenance is not None:
            raise ValueError("known_future_provenance must be None when is_known_future=False")
        return self


