class LagAwareModMRMRResult(BaseModel):
    """Full result payload from a Lag-Aware ModMRMR covariate-lag selection run.

    This is the top-level result returned by ``run_lag_aware_mod_mrmr()``.

    Attributes:
        config: The configuration used for this selection run.
        selected: Ordered list of selected lagged features; ordered by
            ``selection_rank`` ascending.
        rejected: List of legal candidates that were not selected.
        blocked: List of candidates that were blocked before scoring due to
            forecast-horizon legality failures.
        n_candidates_evaluated: Total number of candidates that entered the
            scorer (legal candidates, excluding blocked ones).
        n_candidates_blocked: Total number of candidates blocked due to
            lag-cutoff violations.
        relevance_scorer_spec: Specification of the scorer used for relevance.
        redundancy_scorer_spec: Specification of the scorer used for redundancy.
        target_history_scorer_spec: Specification of the scorer used for
            target-history novelty; ``None`` when the penalty is disabled.
        notes: Human-readable notes about the selection run (e.g. degenerate
            inputs, parameter overrides, or interpretation caveats).
    """

    model_config = ConfigDict(frozen=True)

    config: LagAwareModMRMRConfig = Field(
        description="Configuration used for this selection run.",
    )
    selected: list[SelectedLagAwareFeature] = Field(
        default_factory=list,
        description="Ordered list of selected features by selection_rank ascending.",
    )
    rejected: list[RejectedLagAwareFeature] = Field(
        default_factory=list,
        description="Legal candidates that were not selected.",
    )
    blocked: list[BlockedLagAwareFeature] = Field(
        default_factory=list,
        description="Candidates blocked due to forecast-horizon legality failures.",
    )
    n_candidates_evaluated: int = Field(
        ge=0,
        description=(
            "Total number of legal candidates that entered the scorer "
            "(excluding blocked candidates)."
        ),
    )
    n_candidates_blocked: int = Field(
        ge=0,
        description="Total number of candidates blocked due to lag-cutoff violations.",
    )
    relevance_scorer_spec: PairwiseScorerSpec = Field(
        description="Scorer specification used for relevance scoring.",
    )
    redundancy_scorer_spec: PairwiseScorerSpec = Field(
        description="Scorer specification used for redundancy scoring.",
    )
    target_history_scorer_spec: PairwiseScorerSpec | None = Field(
        default=None,
        description=(
            "Scorer specification used for target-history novelty; "
            "None when the penalty is disabled."
        ),
    )
    notes: list[str] = Field(
        default_factory=list,
        description="Human-readable notes about the selection run.",
    )

    @model_validator(mode="after")
    def _validate_selected_rank_order(self) -> LagAwareModMRMRResult:
        """Require selected features to be ordered by selection_rank ascending."""
        for i, feature in enumerate(self.selected):
            if feature.selection_rank != i + 1:
                raise ValueError(
                    f"selected[{i}].selection_rank must be {i + 1}, got {feature.selection_rank}"
                )
        return self

    @model_validator(mode="after")
    def _validate_counts_consistent(self) -> LagAwareModMRMRResult:
        """Require n_candidates_evaluated and n_candidates_blocked to be consistent."""
        actual_evaluated = len(self.selected) + len(self.rejected)
        if self.n_candidates_evaluated != actual_evaluated:
            raise ValueError(
                f"n_candidates_evaluated ({self.n_candidates_evaluated}) must equal "
                f"len(selected) + len(rejected) ({actual_evaluated})"
            )
        if self.n_candidates_blocked != len(self.blocked):
            raise ValueError(
                f"n_candidates_blocked ({self.n_candidates_blocked}) must equal "
                f"len(blocked) ({len(self.blocked)})"
            )
        return self

    @model_validator(mode="after")
    def _validate_target_history_scorer_consistency(self) -> LagAwareModMRMRResult:
        """Require target_history_scorer_spec consistency with config."""
        config_has_scorer = self.config.target_history_scorer is not None
        result_has_spec = self.target_history_scorer_spec is not None
        if config_has_scorer != result_has_spec:
            raise ValueError(
                "target_history_scorer_spec must be present if and only if "
                "config.target_history_scorer is set"
            )
        return self
