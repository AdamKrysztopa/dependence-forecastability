class ScorerDiagnostics(BaseModel):
    """Diagnostic payload returned by a pairwise scorer for one (x, y) pair.

    Attributes:
        raw_value: Original scorer output before any normalization or clipping.
        normalized_value: Score value used by the selector for relevance or
            similarity; lies in [0, 1] for similarity/redundancy scorers and
            >= 0 for relevance scorers.
        p_value: Unadjusted p-value when significance testing is enabled;
            ``None`` when ``significance_method="none"``.
        adjusted_p_value: Multiple-comparisons-adjusted p-value when
            ``significance_method="bh_fdr_adjustment"``; ``None`` otherwise.
        n_pairs: Number of aligned non-missing sample pairs scored.
        estimator_settings: JSON-serializable estimator settings including kNN,
            surrogate, and kernel parameters where applicable.
        normalization: Normalization strategy name applied to produce
            ``normalized_value``.
        significance_method: Significance testing method used.
        bands: Optional reliability bands, clipping notes, or estimator output
            descriptions associated with this pair.
        warnings: Low-sample, convergence, or estimator-specific warnings.
    """

    model_config = ConfigDict(frozen=True)

    raw_value: float = Field(description="Original scorer output before normalization.")
    normalized_value: float = Field(
        ge=0.0,
        description=(
            "Score used by the selector. For similarity/redundancy scorers this "
            "is clipped to [0, 1]. For relevance scorers this is >= 0."
        ),
    )
    p_value: float | None = Field(
        default=None,
        description="Unadjusted p-value; None when significance_method='none'.",
    )
    adjusted_p_value: float | None = Field(
        default=None,
        description=("BH-adjusted p-value; None unless significance_method='bh_fdr_adjustment'."),
    )
    n_pairs: int = Field(ge=1, description="Number of aligned non-missing sample pairs scored.")
    estimator_settings: dict[str, Any] = Field(
        default_factory=dict,
        description="JSON-serializable estimator settings (kNN, surrogate, kernel params).",
    )
    normalization: NormalizationStrategy = Field(
        description="Normalization strategy applied to produce normalized_value.",
    )
    significance_method: SignificanceMethod = Field(
        description="Significance testing method used for this pair.",
    )
    bands: list[str] = Field(
        default_factory=list,
        description="Reliability bands, clipping notes, or estimator descriptions.",
    )
    warnings: list[str] = Field(
        default_factory=list,
        description="Low-sample, convergence, or estimator-specific warnings.",
    )

    @field_validator("normalized_value")
    @classmethod
    def _validate_normalized_value(cls, v: float) -> float:
        """Ensure normalized_value is non-negative."""
        if v < 0.0:
            raise ValueError(f"normalized_value must be >= 0.0, got {v}")
        return v


