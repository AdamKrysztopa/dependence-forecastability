class LagAwareModMRMRConfig(BaseModel):
    """Configuration for a Lag-Aware ModMRMR covariate-lag selection run.

    Attributes:
        forecast_horizon: Forecast horizon h; the number of steps ahead being
            predicted. Must be >= 1.
        availability_margin: Availability margin m; additional lag buffer
            required beyond the forecast horizon. Must be >= 0. A covariate
            at lag k is legal only when ``k >= forecast_horizon + availability_margin``.
        candidate_lags: Explicit candidate lags to evaluate for each covariate.
            When ``None``, the selector derives lags from ``1`` to ``max_lag``.
        max_lag: Maximum lag horizon when ``candidate_lags`` is ``None``.
            Must be >= 1.
        known_future_covariates: Mapping of covariate names to their
            known-future provenance declaration. Covariates in this mapping
            bypass the ordinary lag-cutoff filter. Only values that are
            genuinely known at forecast creation time are valid; realized
            future observations must not be entered here.
        target_lags: Explicit target-history lags used for the novelty penalty.
            Target-history lags must also satisfy
            ``lag >= forecast_horizon + availability_margin``. When ``None``,
            the target-history novelty penalty is disabled.
        max_selected_features: Maximum number of features to include in the
            selected set across all covariates. Must be >= 1.
        relevance_floor: Minimum normalized relevance below which a candidate
            is rejected before any redundancy penalty. Must be in [0, 1).
        relevance_scorer: Scorer spec for computing relevance of each candidate
            against the target.
        redundancy_scorer: Scorer spec for computing similarity between
            candidates and already-selected features.
        target_history_scorer: Scorer spec for computing similarity between
            candidates and target-history lags. When ``None``, the
            target-history novelty penalty is disabled.
    """

    model_config = ConfigDict(frozen=True)

    forecast_horizon: int = Field(ge=1, description="Forecast horizon h (steps ahead).")
    availability_margin: int = Field(
        default=0,
        ge=0,
        description=(
            "Extra lag buffer beyond the forecast horizon. "
            "Covariate lag k is legal when k >= forecast_horizon + availability_margin."
        ),
    )
    candidate_lags: list[int] | None = Field(
        default=None,
        description=(
            "Explicit candidate lags per covariate. None means use range(1, max_lag + 1)."
        ),
    )
    max_lag: int = Field(
        default=20,
        ge=1,
        description="Maximum lag horizon when candidate_lags is None.",
    )
    known_future_covariates: dict[str, KnownFutureProvenance] = Field(
        default_factory=dict,
        description=(
            "Covariate name → known-future provenance. "
            "Covariates listed here bypass the ordinary lag-cutoff. "
            "Realized future observations must not be entered."
        ),
    )
    target_lags: list[int] | None = Field(
        default=None,
        description=(
            "Target-history lags for the novelty penalty. "
            "Must satisfy lag >= forecast_horizon + availability_margin. "
            "None disables the target-history novelty penalty."
        ),
    )
    max_selected_features: int = Field(
        default=10,
        ge=1,
        description="Maximum number of features to include in the selected set.",
    )
    relevance_floor: float = Field(
        default=0.0,
        ge=0.0,
        lt=1.0,
        description=(
            "Minimum normalized relevance. Candidates at or below this floor are "
            "rejected before any redundancy penalty is applied."
        ),
    )
    relevance_scorer: PairwiseScorerSpec = Field(
        description="Scorer spec for relevance of each candidate against the target.",
    )
    redundancy_scorer: PairwiseScorerSpec = Field(
        description=(
            "Scorer spec for similarity between candidates and already-selected features."
        ),
    )
    target_history_scorer: PairwiseScorerSpec | None = Field(
        default=None,
        description=(
            "Scorer spec for similarity between candidates and target-history lags. "
            "None disables the target-history novelty penalty."
        ),
    )

    @field_validator("candidate_lags")
    @classmethod
    def _validate_candidate_lags(cls, v: list[int] | None) -> list[int] | None:
        """Require all candidate lags to be positive integers when provided."""
        if v is None:
            return None
        if len(v) == 0:
            raise ValueError("candidate_lags must be non-empty when provided")
        if any(lag < 1 for lag in v):
            raise ValueError("All candidate_lags must be >= 1")
        return v

    @field_validator("target_lags")
    @classmethod
    def _validate_target_lags(cls, v: list[int] | None) -> list[int] | None:
        """Require all target-history lags to be positive integers when provided."""
        if v is None:
            return None
        if len(v) == 0:
            raise ValueError("target_lags must be non-empty when provided")
        if any(lag < 1 for lag in v):
            raise ValueError("All target_lags must be >= 1")
        return v

    @model_validator(mode="after")
    def _validate_target_history_consistency(self) -> LagAwareModMRMRConfig:
        """Require target_history_scorer when target_lags is set, and vice-versa."""
        has_lags = self.target_lags is not None
        has_scorer = self.target_history_scorer is not None
        if has_lags and not has_scorer:
            raise ValueError("target_history_scorer must be provided when target_lags is set")
        if has_scorer and not has_lags:
            raise ValueError("target_lags must be provided when target_history_scorer is set")
        return self


