class PairwiseScorerSpec(BaseModel):
    """Specification describing a pairwise scorer used in a selection run.

    Attributes:
        name: Human-readable scorer name (e.g. ``"catt_knn_mi"``).
        backend: Underlying estimator or library backing this scorer.
        normalization: Normalization strategy applied to raw values.
        significance_method: Significance testing method for relevance scores.
        settings: Additional scorer settings (kNN ``k``, n_surrogates, etc.).
    """

    model_config = ConfigDict(frozen=True)

    name: str = Field(description="Scorer identifier (e.g. 'catt_knn_mi', 'spearman_abs').")
    backend: str = Field(
        description="Underlying estimator or library (e.g. 'ksg', 'sklearn', 'scipy').",
    )
    normalization: NormalizationStrategy = Field(
        description="Normalization strategy applied to raw scorer values.",
    )
    significance_method: SignificanceMethod = Field(
        description="Significance testing applied when scoring relevance.",
    )
    settings: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional scorer settings (n_neighbors, n_surrogates, alpha, etc.).",
    )

    @field_validator("name")
    @classmethod
    def _validate_name(cls, v: str) -> str:
        """Require non-empty scorer name."""
        if not v.strip():
            raise ValueError("PairwiseScorerSpec.name must be non-empty")
        return v

    @field_validator("backend")
    @classmethod
    def _validate_backend(cls, v: str) -> str:
        """Require non-empty backend identifier."""
        if not v.strip():
            raise ValueError("PairwiseScorerSpec.backend must be non-empty")
        return v


