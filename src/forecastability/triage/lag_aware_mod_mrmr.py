"""Domain contracts for Lag-Aware ModMRMR covariate informativeness triage.

This module defines the frozen Pydantic result models for the v0.4.3 Lag-Aware
ModMRMR sparse covariate-lag selector.

ModMRMR is a project-defined mRMR variant proposed by Adam Krysztopa.
It modifies the redundancy part of mRMR-style greedy selection by using
multiplicative maximum-similarity suppression against already-selected features.

All models in this module are frozen (immutable after construction) and use
closed Literal label fields for categorical outputs to support deterministic
serialisation and regression fixture comparison.
"""

from __future__ import annotations

from typing import Any, Literal

from pydantic import BaseModel, ConfigDict, Field, field_validator, model_validator

# ---------------------------------------------------------------------------
# Type aliases — closed Literal labels
# ---------------------------------------------------------------------------

KnownFutureProvenance = Literal[
    "calendar",
    "schedule",
    "contractual",
    "forecasted_input",
]
"""Provenance of a known-future covariate.

- ``calendar``: calendar flag or indicator (e.g. day-of-week, holiday).
- ``schedule``: planned production, maintenance, or operational schedule.
- ``contractual``: setpoint or setpoint envelope fixed by contract.
- ``forecasted_input``: value generated by an external forecast at creation time.
"""

LagLegalityLabel = Literal[
    "legal",
    "blocked_lag_too_small",
    "blocked_known_future",
    "legal_known_future",
]
"""Legality classification of a lagged covariate candidate.

- ``legal``: lag >= forecast_horizon + availability_margin.
- ``blocked_lag_too_small``: lag < forecast_horizon + availability_margin.
- ``blocked_known_future``: covariate claims known-future but context is a
  realized future observation.
- ``legal_known_future``: covariate is declared known-future with valid
  provenance and bypass is explicit.
"""

NormalizationStrategy = Literal[
    "rank_percentile",
    "surrogate_effect_clip",
    "nmi_min_entropy",
    "nmi_mean_entropy",
    "none",
]
"""Normalization strategy applied to raw scorer values before selection.

- ``rank_percentile``: rank-percentile normalization within the comparison pool.
- ``surrogate_effect_clip``: converts observed-vs-surrogate effect to [0, 1].
- ``nmi_min_entropy``: MI / min(Hx, Hy) when entropy estimates are available.
- ``nmi_mean_entropy``: 2*MI / (Hx + Hy) when entropy estimates are available.
- ``none``: no normalization; raw scorer value is used as-is.
"""

SignificanceMethod = Literal[
    "none",
    "upper_tail_mi_surrogate",
    "bh_fdr_adjustment",
]
"""Significance method applied to relevance scores.

- ``none``: no significance testing; all candidates enter selection.
- ``upper_tail_mi_surrogate``: upper-tail surrogate MI test with n_surrogates >= 99.
- ``bh_fdr_adjustment``: Benjamini–Hochberg FDR correction across all candidates.
"""

RejectionReason = Literal[
    "below_relevance_floor",
    "zero_final_score",
    "max_features_reached",
    "dominated_by_selected",
]
"""Reason a legal candidate was rejected during greedy selection.

- ``below_relevance_floor``: relevance <= relevance_floor before scoring.
- ``zero_final_score``: final score rounded to zero after penalty application.
- ``max_features_reached``: max_selected_features was reached before this candidate.
- ``dominated_by_selected``: candidate is not selected due to greedy ordering.
"""


